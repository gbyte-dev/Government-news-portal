            button.addEventListener('click', function() {
                if (this.textContent.includes('Soon')) {
                    this.style.background = '#ddd';
                    this.textContent = 'Coming Soon...';
                    setTimeout(() => {
                        this.style.background = '';
                        this.textContent = this.textContent.replace('Coming Soon...', 'Apply Online (Soon)');
                    }, 2000);
                }
            });
        });

        // Add scroll animations
        const observerOptions = {
            threshold: 0.1,
            rootMargin: '0px 0px -100px 0px'
        };

        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.style.opacity = '1';
                    entry.target.style.transform = 'translateY(0)';
                }
            });
        }, observerOptions);

        document.querySelectorAll('.section').forEach(section => {
            section.style.opacity = '0';
            section.style.transform = 'translateY(20px)';
            section.style.transition = 'opacity 0.6s ease, transform 0.6s ease';
            observer.observe(section);
        });

        // Add current date update
        function updateCurrentDate() {
            const now = new Date();
            const dateString = now.toLocaleDateString('en-IN', {
                day: 'numeric',
                month: 'long',
                year: 'numeric'
            });
            console.log('Current Date:', dateString);
        }

        updateCurrentDate();

        // Add countdown timer for application start date
        function startCountdown() {
            const targetDate = new Date('2025-07-07').getTime();
            
            const timer = setInterval(() => {
                const now = new Date().getTime();
                const distance = targetDate - now;
                
                if (distance < 0) {
                    clearInterval(timer);
                    return;
                }
                
                const days = Math.floor(distance / (1000 * 60 * 60 * 24));
                const hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
                const minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
                
                console.log(`Time until application starts: ${days}d ${hours}h ${minutes}m`);
            }, 60000);
        }

        startCountdown();
    </script>