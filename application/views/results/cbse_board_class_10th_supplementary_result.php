        <div class="section">
            <h2>Eligibility Criteria</h2>
            <ol class="ol-details">
                <li class="li-details">Minimum 75% attendance is required. Relaxation may be granted for genuine reasons.</li>
                <li class="li-details">Minimum 33% marks in each subject required to pass.</li>
                <li class="li-details">For practical subjects, 33% required in both theory and practical separately.</li>
                <li class="li-details">Students must pass internal assessments (grade higher than 'E').</li>
                <li class="li-details">Students failing in 1 or 2 subjects are eligible for compartment exams.</li>
                <li class="li-details">Subject choices must be finalized before LOC submission. Changes afterward are not permitted.</li>
            </ol>
