                            <td>Primary School Teacher (Post Code 1 to 4)</td>
                            <td>Candidates must have passed the MP TET (2020 or 2024) and meet one of the following: 12th with 50% marks + 2-year D.El.Ed. or Special Education; 12th with 45% + D.El.Ed. (as per NCTE 2002); 12th with 50% + 4-year B.El.Ed.; or Graduation + 2-year D.El.Ed. Reserved categories get 5% relaxation in marks. All qualifications must be from recognized institutions, and required documents must be submitted on time.</td>
                        </tr>
                        <tr>
                            <td>Primary School Teacher - Science (Post Code 5 to 10)</td>
                            <td>Candidates must have at least 50% marks in 12th (Science stream) and either a 2-year Diploma in Elementary/Special Education or a 4-year B.El.Ed. with the relevant subject. SC, ST, OBC, and PwD candidates get 5% relaxation in marks. All qualifications must be from recognized institutions, and required certificates must be verified and submitted on time.</td>
