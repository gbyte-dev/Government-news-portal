            <h2>Section-wise Syllabus</h2>
            <ul class="ul-details">
                <li class="li-details"><strong>General Hindi:</strong> वर्णमाला, व्याकरण, पर्यायवाची, विलोम शब्द, अपठित बोध, उपसर्ग/प्रत्यय, सन्धि, समास, वाक्य निर्माण, लोकोक्तियाँ, मुहावरे, वर्तनी, वाक्य संशोधन, रस, छन्द, अलंकार, प्रसिद्ध लेखक एवं रचनाएं, पुरस्कार आदि।</li>
                <li class="li-details"><strong>General Knowledge:</strong> भारत एवं पड़ोसी देश, विज्ञान, पुरस्कार, भाषाएं, किताबें एवं लेखक, राजधानी एवं मुद्रा, खेल, महत्वपूर्ण खिलाड़ी।</li>
                <li class="li-details"><strong>Law & Constitution:</strong> मानवाधिकार, यातायात नियम, अपराध व दंड के सिद्धांत, आत्मरक्षा, भारतीय संविधान, मौलिक अधिकार, राज्य के नीति निदेशक तत्व, संशोधन, महिला एवं बाल कानून, आरक्षण कानून, पर्यावरण कानून आदि।</li>
                <li class="li-details"><strong>Numerical Ability:</strong> संख्या पद्धति, सरलीकरण, HCF, LCM, दशमलव, भिन्न, ब्याज, लाभ-हानि, समय-कार्य, अनुपात-प्रमाण, प्रतिशत, क्षेत्रमिति आदि।</li>
                <li class="li-details"><strong>Mental Ability / IQ Test:</strong> संबंध, सीरीज, कोडिंग-डीकोडिंग, दिशा ज्ञान, रक्त संबंध, वेन डायग्राम, गणितीय योग्यता, क्रमबद्ध करना।</li>
                <li class="li-details"><strong>Mental Aptitude:</strong> कानून व्यवस्था, सांप्रदायिक सौहार्द, अपराध नियंत्रण, अनुकूलन क्षमता, पुलिस प्रणाली, व्यावसायिक जानकारी, मानसिक मजबूती, संवेदनशीलता आदि।</li>
                <li class="li-details"><strong>Reasoning Ability:</strong> स्मृति, अंतर, समानताएं, स्थानिक दृष्टि, अवधारणाएं, गणितीय तर्कशक्ति, समस्या समाधान, निर्णय क्षमता, विश्लेषण।</li>
            </ul>
